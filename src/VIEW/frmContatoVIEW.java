    private void listarValoresContato(){
        try {
            ContatoDAO objcontatodao = new ContatoDAO();
            
            DefaultTableModel model = (DefaultTableModel) tabelaContato.getModel();
            
            model.setNumRows(0);
            
            ArrayList<ContatoDTO> lista = objcontatodao.PesquisarContato();
            
            for(int i=0;i < lista.size();i++){
                model.addRow(new Object[]{
                lista.get(i).getId(),
                lista.get(i).getNome(),
                lista.get(i).getEmail(),
                lista.get(i).getTelefone()
            });
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null,"frmContatoListarValores"+ e);
        }
    }
    
    
    
    private void CarregarCampos(){
        int setar = tabelaContato.getSelectedRow();
        
        txtId.setText(tabelaContato.getModel().getValueAt(setar, 0).toString());
        txtNome.setText(tabelaContato.getModel().getValueAt(setar, 1).toString());
        txtEmail.setText(tabelaContato.getModel().getValueAt(setar, 2).toString());
        txtTelefone.setText(tabelaContato.getModel().getValueAt(setar, 3).toString());
    }
    
    private void CadastrarContatos(){
        String nome,telefone,email;
        
        nome = txtNome.getText();
        telefone = txtTelefone.getText();
        email = txtEmail.getText();
        
        ContatoDTO objcontatodto = new ContatoDTO();
        objcontatodto.setNome(nome);
        objcontatodto.setEmail(email);
        objcontatodto.setTelefone(telefone);
        
        ContatoDAO objcontatodao = new ContatoDAO();
        objcontatodao.cadastrarContato(objcontatodto);
        
        listarValoresContato();
            
    }
    
    public void AtualizarContatos(){
        String nome,email,telefone;
        int id;
        
        nome = txtNome.getText();
        telefone = txtTelefone.getText();
        email = txtEmail.getText(); 
        id = Integer.parseInt(txtId.getText());
        
        try {
            if(!txtId.getText().isEmpty()){
                ContatoDTO objcontatodto = new ContatoDTO();
                objcontatodto.setNome(nome);
                objcontatodto.setEmail(email);
                objcontatodto.setTelefone(telefone);
                objcontatodto.setId(id);
        
                ContatoDAO objcontatodao = new ContatoDAO();
                objcontatodao.EditarContato(objcontatodto);
        
                listarValoresContato();
            }
        } catch (Exception erro) {
            JOptionPane.showMessageDialog(null, "Botao Atualizar"+erro);
        }
            
    }
    
    public void DeletarContatos(){
        int id;
                
                id = Integer.parseInt(txtId.getText());
                
                ContatoDTO objcontatodto = new ContatoDTO();
                objcontatodto.setId(id);
                
                ContatoDAO objcontatodao = new ContatoDAO();
                objcontatodao.DeletarContato(objcontatodto);
                
                listarValoresContato();
    }
    
    public void LimparCampos(){
        txtId.setText("");
        txtNome.setText("");
        txtEmail.setText("");
        txtTelefone.setText("");
        txtNome.requestFocus();
    }

